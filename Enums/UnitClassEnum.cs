namespace AgeOfWar.Enums
{
    /// <summary>
    /// Defines the different classes of military units available in the game.
    /// </summary>
    public enum UnitClassEnum
    {
        /// <summary>
        /// Represents basic militia soldiers.
        /// </summary>
        Militia,

        /// <summary>
        /// Represents soldiers armed with spears.
        /// </summary>
        Spearmen,

        /// <summary>
        /// Represents lightly armored cavalry units.
        /// </summary>
        LightCavalry,

        /// <summary>
        /// Represents heavily armored cavalry units.
        /// </summary>
        HeavyCavalry,

        /// <summary>
        /// Represents infantry archers.
        /// </summary>
        FootArcher,

        /// <summary>
        /// Represents mounted archers or cavalry equipped with bows.
        /// </summary>
        CavalryArcher
    }
}