namespace AgeOfWar.Enums
{
    /// <summary>
    /// Represents the possible outcomes of a battle or combat between platoons.
    /// </summary>
    public enum ResultEnum
    {
        /// <summary>
        /// Indicates the attacker or player won the battle.
        /// </summary>
        Win,

        /// <summary>
        /// Indicates the battle ended in a draw or stalemate.
        /// </summary>
        Draw,

        /// <summary>
        /// Indicates the attacker or player lost the battle.
        /// </summary>
        Loss
    }
}